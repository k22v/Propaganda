            course = Course(**course_data, is_published=True)
            db.add(course)
            await db.flush()
            
            order_sec = 1
            for section_data in sections:
                chapters = section_data.pop("chapters")
                section = Section(course_id=course.id, title=section_data["title"], order=order_sec)
                db.add(section)
                await db.flush()
                
                order_ch = 1
                for chapter_data in chapters:
                    contents = chapter_data.pop("contents")
                    chapter = Chapter(section_id=section.id, title=chapter_data["title"], order=order_ch)
                    db.add(chapter)
                    await db.flush()
                    
                    order_cont = 1
                    for content in contents:
                        lesson = LessonContent(
                            chapter_id=chapter.id,
                            title=content["title"],
                            content_type=content["content_type"],
                            content=content.get("content", ""),
                            video_url=content.get("video_url"),
                            order=order_cont
                        )
                        db.add(lesson)
                        await db.flush()
                        
                        if content["content_type"] == "quiz" or "quiz" in content:
                            pass
                        
                        order_cont += 1
                    order_ch += 1
